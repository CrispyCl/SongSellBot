@router.callback_query(F.data == "to_main")
async def to_main_menu(callback: CallbackQuery, state: FSMContext, current_user: User, bot: Bot):
    await state.clear()
    await process_start_command(callback.message, state, current_user=current_user, bot=bot)
    await callback.answer()
    await callback.message.delete()  # type: ignore


